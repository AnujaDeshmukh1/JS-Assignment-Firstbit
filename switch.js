const code = 'INR'
switch(code){
    case 'INR' :
        console.log("Indian Rupee");
        break;
    case 'CAD' :
        console.log("Canadian Dollor");
        break;
    case 'JPY' :
        console.log("Yen");
        break;
    case 'MYR' :
        console.log("Malaysian Ringgit");
        break;
    default:
        console.log("Please enter valid country code");
}